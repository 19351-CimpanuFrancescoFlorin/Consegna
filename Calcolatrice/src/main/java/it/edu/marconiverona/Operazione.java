public class Operazione {

    public int somma(int a, int b) {
        return a + b;
    }
    
    public int differenza(int a, int b) {
        return a - b;
    }
    
    public int moltiplicazione(int a, int b) {
        return a * b;
    }
    
    public float divisione(float a, float b) {
        return a / b;
    }

    // costruttore 
    public Operazione() {

    }

}
